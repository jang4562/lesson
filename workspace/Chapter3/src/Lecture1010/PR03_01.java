package Lecture1010;

public class PR03_01 {

	public static void main(String[] args) {
	int A,B ;
	A = 100;
	B = 200;
	System.out.print(A + "+" + B + "=");
	System.out.printf("%d", 100+200);
	System.out.printf("\n");
	System.out.print(A + " " + "/" + " " + B + " " + "=" + " " );
	System.out.printf("%f", 100.0/200);
	System.out.printf("\n");
	System.out.printf("%d + %d = %d\n", 100, 200, 100+200);
	System.out.printf("%d / %d = %f\n", 100, 200, 100/200.0);



	
	}

}
